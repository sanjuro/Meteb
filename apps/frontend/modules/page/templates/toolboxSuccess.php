            <div class="AccordionPanel">
              <div class="AccordionPanelTab">Post Retirement Interest Rate (PRI)</div>
              <div class="AccordionPanelContent">
                <p>The PRI rate you choose will  determine your starting annuity income and it will affect your future annual  increases. The PRI can be seen as the upfront rate of future returns you want  your starting annuity to take credit for. This is because only investment  returns in excess of your PRI are available for your pension increases.<br />
                  The higher the chosen PRI:</p>
                <ul>
                  <li>The higher the starting level  of income will be</li>
                  <li>But the lower future increase  will be.</li>
                </ul>
<p>The chart below shows how your  annuity income may grow over time for two different PRI categories. Please note  your actual future increases may differ to the chart below and that past  performance is no guarantee of future results. Your own starting pension will  depend on your particulars such as the amount available for investment; the  options you choose; your age and sex. </p>
<p><img src="toolbox/annuitycomparison.jpg" width="650" height="327" alt="annuity comparison" /></p>
                <p>Your annual increase comes from  the investment profits that remain after your PRI has been met.  <br />
Consider for example:</p>
                <ul>
                  <li>Pensioner on 4% PRI</li>
                  <li>Investment returns of 10%</li>
                  <li>Then 4% of these returns will  be used to maintain your current level of income</li>
                  <li>The remaining 6% (10% - 4%)  will be used to increase your pension.</li>
                </ul>
                <p>It  is only natural to want the highest PRI which gives the highest initial income,  but you shouldn’t forget the effects of lower future increases which could  erode your purchasing power. A with-profit annuity with a lower PRI will start  lower, but it will potentially award higher increases in the future.</p>
              </div>
            </div>
            <div class="AccordionPanel">
              <div class="AccordionPanelTab"> Single or Joint Life </div>
              <div class="AccordionPanelContent">
                <p>A single life annuity pays a  higher level of income than a joint life annuity, but stops when you pass away  (if no guarantee period was chosen).<br />
                  If you choose a joint life  annuity the pension payments will continue to your spouse or common-law partner  if you pass away first. You can choose how much income your spouse will receive  after you have died by selecting a spouse’s reversion %. <br />
                  For example, a 75% spouse’s  reversion means that when you pass away, your spouse will receive 75% of your  pension for as long as they live.</p>
              </div>
            </div>
            <div class="AccordionPanel">
              <div class="AccordionPanelTab">2.3	Guarantee Period</div>
              <div class="AccordionPanelContent">
                <p>A guarantee period allows you to  guarantee your annuity to be paid for a certain period of time. This means that  if you pass away soon after investing, your nominated beneficiaries or estate  will continue to receive income payments.<br />
                  You can choose 5 or 10 year  guaranteed periods or no guarantee at all. If you select a 5 year guarantee  period and passed away after 3 years, your income will continue to be paid to  your dependants for another 2 years. If you select a 10 year guarantee, the  income payments would continue for another 7 years.</p>
                <p>If  you invested in our with-profit annuity without a guarantee period and died the  day after, your income payments will stop and your family would get nothing  (unless a joint life annuity was selected). It is important to note that in  this scenario your investment will be shared as mortality profits and will be  distributed to the other with-profit pensioners by way of enhanced increases.  By choosing a guaranteed period you can protect yourself and your family  against this rare event.</p>
              </div>
