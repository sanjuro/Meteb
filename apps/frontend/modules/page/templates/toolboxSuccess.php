              <div class="AccordionPanelTab">Glossary</div>
              <div class="AccordionPanelContent">
              <p><strong>Annuity </strong>An agreement between an insurer and a policyholder  whereby the insurer makes regular payments to the policyholder throughout the  life of the consumer.<br />
                  <strong>Balanced Fund </strong>A fund that has investments diversified over  equities, bonds, property and money market instruments.<br />
                  <strong>Bonus rate </strong>The net rate of return declared on the with-profit  annuity assets from which annual increases are determined.<br />
                  <strong>Consumer Price Index (CPI) </strong>An indicator used to  monitor inflation, also referred to as the cost-of-living index. <br />
                  <strong>Guarantee period </strong>The minimum period that  you want your annuity to be paid out for certain. Should you pass away within  the guarantee period, your annuity will continue to be paid to your nominated  beneficiaries or estate.<br />
                  <strong>Inflation </strong>A rise in the general price level of goods and  services, which leads to decreasing purchasing power of the currency concerned. <br />
                  <strong>Insurer / insurance company </strong>A company that agrees to pay the consumer an amount  or amounts on the occurrence of an event that is defined in the contract with  the policyholder.<br />
                  <strong>Investment profits / investment return </strong>The money that has been made from an investment in  assets. This can include growth in the value of an asset or dividends paid to  the investor.<br />
                  <strong>Joint life annuity </strong>An annuity that is  payable to the annuitant and his/her spouse until both individuals have passed  away. (Compare with Single life annuity)<br />
                  <strong>Mortality experience </strong>See Mortality profits<br />
                  <strong>Mortality profits </strong>A profit or loss incurred  by the insurer as a result of differences in the actual length of policyholder  lifetimes and the assumed length of such lifetimes. To provide an annuity for  the life of policyholders, the insurer needs to make an assumption about the expected  length of policyholder lifetimes. <br />
                  <strong>Post-retirement interest  (PRI) rate</strong> A rate that represents  the minimum amount of investment profit or returns the portfolio underlying the  with-profit annuity has to achieve to continue paying the current level of  pension per year. <br />
                  <strong>Purchasing Power </strong>The amount of goods or  services that one unit of money can buy for the currency concerned. In general,  inflation decreases the amount of goods or services you would be able to  purchase.<br />
                  <strong>Single life annuity </strong>An annuity that is  payable only to the main member and the payments stop upon his/her death. <br />
                  <strong>Smoothing </strong>A technique Momentum  applies to investment profits in order to provide stable increases from one  year to the next. <br />
                  <strong>Socially responsible investment (SRI)</strong> One of the asset classes  in our with-profit annuity assets. In order for an investment to be classified  as an SRI it must meet a number of criteria, such as a business's commitment to  black economic empowerment, addressing HIV/Aids in the workplace, labour  policies and environmental practices. <strong></strong><br />
                  <strong>With-Profit Annuity </strong>An annuity contract that allows policyholders to  share in the profits made on the with-profit assets. This profit-sharing is done  by way of future annual increases. </p>
